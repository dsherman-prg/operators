fun main(){
    // Initialize variables
    // Result might be redundant, but I like the organization
    val a = 10
    val b = 2
    var result = 0

    // Perform calculations and assign it to result
    // Print the operation and the result
    result = a+b
    println("$a + $b = $result")

    result = a/b
    println("$a / $b = $result")

    result = a*b
    println("$a * $b = $result")

    result = a-b
    println("$a - $b = $result")

    result = a%b
    println("$a % $b = $result")
}