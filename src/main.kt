fun main(){
    val a = 10
    val b = 2
    var result = 0

    result = a+b
    println("$a + $b = $result")

    result = a/b
    println("$a / $b = $result")

    result = a*b
    println("$a * $b = $result")

    result = a-b
    println("$a - $b = $result")

    result = a%b
    println("$a % $b = $result")
}