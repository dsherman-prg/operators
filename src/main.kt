fun main(){
    // Initialize variables
    val a = 10
    val b = 2

    // Perform calculations and assign it to result
    // Print the operation and the result
    var result = a+b
    println("$a + $b = $result")

    result = a/b
    println("$a / $b = $result")

    result = a*b
    println("$a * $b = $result")

    result = a-b
    println("$a - $b = $result")

    result = a%b
    println("$a % $b = $result")
}